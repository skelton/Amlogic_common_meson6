#define PHO_DAT_OFFSET              (0X10C)
#define PHO_PULL1_OFFSET             (0X11C)

#define GSL1680_I2C_NAME 	"gsl1680"
#define GSL_DATA_REG		0x80
#define GSL_STATUS_REG		0xe0
#define GSL_PAGE_REG		0xf0

#define GPIOF_CON			0x7f0080a0
#define GPIOF_DAT			0x7f0080a4
#define GPIOF_PUD			0x7f0080a8

#define READ_BUF_LEN		4
#define WRITE_BUF_LEN		5

#ifndef I2C_MAJOR
#define I2C_MAJOR			125
#endif
#define I2C_MINORS			256

#define SCREEN_MAX_X 		800
#define SCREEN_MAX_Y 		480
#define PRESS_MAX    		255
#define MAX_CONTACTS 		5

#define DMA_TRANS_LEN		0x20

#define NUM_PER_SEND		32 // 2 4 8 16 32 you can chose (DMA)


struct gsl1680_platform_data {
	const char *ts_name;
	u32 dis_min_x; /* display resoltion */
	u32 dis_max_x;
	u32 dis_min_y;
	u32 dis_max_y;
	u32 min_touch; /* no.of touches supported */
	u32 max_touch;
	u32 min_tid; /* track id */
	u32 max_tid;
	u32 min_width;/* size of the finger */
	u32 max_width;
	u32 res_x; /* TS resolution */
	u32 res_y;
	u32 swap_xy;
	u32 flags;
	u16 invert_x;
	u16 invert_y;
	u8 nfingers;

	int (*suspend)(void);	/* call when suspend */
	int (*resume)(void); /* call when resume */

	int (*io_init)(void); /* call when probe start */
	int (*power)(int on); /* call after io_init */
	int (*chip_init)(void); /* call after power on */
	int (*io_deinit)(void); /* call when probe fail */
};


